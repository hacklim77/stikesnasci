					<p>
						STIKES NASIONAL merupakan perguruan tinggi yang diselenggarakan oleh Yayasan Pendidikan Pharmasi Nasional Surakarta yang telah berdiri sejak tahun 12 September 1957. Perjuangan, dedikasi, komitment untuk mencerdaskan dan menyehatkan anak bangsa telah menghasilkan ribuan alumni berkualitas dan berintegritas yang tersebar di seluruh Indonesia  baik ditingkat pendidikan menengah yaitu SMF/SMK Nasional, SMAK Nasional maupun jenjang pendidikan tinggi yaitu  AAK Nasional (1997) dan AKFAR Nasional (2004). Pada tahun 2016,  AAK dan AKFAR Nasional dilebur  dan diubah bentuknya menjadi STIKES NASIONAL  berdasarkan Surat Keputusan Menteri Riset, Teknologi dan Pendidikan Tinggi Republik Indonesia nomor 116/KPT/I/2016 tanggal 10 Maret 2016. Pada awal perkembangannya STIKES Nasional terdiri dari 5 program studi yaitu S1 Farmasi, D IV Fisioterapi, D IV Teknologi Laboratorium Medik/Analis Kesehatan, D III Farmasi dan D III Analis Kesehatan.  Program studi â€“program studi akan terus dikembangkan menurut kebutuhan masyarakat dan perkembangan ilmu dan teknologi yang ada baik pada kualitasnya maupun kuantitasnya. Visi STIKES NASIONAL adalah menjadi pusat pengembangan pendidikan  tenaga kesehatan yang terpercaya.  Sejalan dengan visi tersebut seluruh program studi di STIKES NASIONAL  telah menerapkan sistem penjaminan mutu  ISO 9001:2008 oleh United Registrar of System dengan sertifikat No: 46092/A/0001/U/En dan setifikat No.46092/A/0001/UK/En dan telah terakreditasi  oleh BAN-PT dan atau LAM-PT (Lembaga Akreditasi Mandiri Perguruan Tinggi).
