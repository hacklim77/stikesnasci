						<b>NILAI-NILAI DASAR</b><br>
						Nilai-nilai dasar (Core value) STIKES Nasional adalah sebagai berikut :
						<table class="w-full va-top">
							<tr>
								<td>1</td>
								<td>.</td>
								<td>
									<b>Competitive.</b> Memiliki daya saing, keunggulan kompetitif (competitive advantage) yaitu kemampuan yang diperoleh melalui karakteristik dan sumber daya dari STIKES Nasional untuk memiliki kinerja yang lebih tinggi dibandingkan perguruan tinggi lain sejenis.
								</td>
							</tr>
							<tr>
								<td>2</td>
								<td>.</td>
								<td>
									<b>Competent.</b> Memiliki kecakapan, keahlian dan wewenang untuk menentukan sesuatu.
									<ul>
										<li>
											<i>Understanding</i> atau pemahaman, yaitu kedalaman kognitif yang dimiliki.	
										</li>
										<li>
											<i>Skill</i> atau kemampuan, yaitu sesuatu ketrmpilan atau bakat yang dimiliki oleh indivindu untuk melakukan pekerjaan yang dibebankan kepadanya.
										</li>
										<li>
											<i>Knowledge</i> atau pengetahuan, yaitu kesadaran dalam bidang kognitif, yaitu mengetahui apa yang harus diperbuat.
										</li>
										<li>
											<i>Interest</i> atau minat, yaitu kecenderungan seseorang yang tinggi terhadap sesuatu atau untuk melakukan sesuatu perbuatan.
										</li>
										<li>
											<i>Attitude</i> atau sikap, yaitu reaksi seseorang terhadap rangsangan yang datang dari luar.
										</li>
										<li>
											<i>Value</i> atau nilai, yaitu suatu standar perilaku atau sikap yang dipercayai secara psikologis telah menyatu dalam diri seseorang.
										</li>
									</ul>
								</td>
							</tr>
							<tr>
								<td>3</td>
								<td>.</td>
								<td>
									<b>Trustworthy.</b> Dapat dipercaya, dapat diandalkan, memiliki karakter/watak yang jujur, loyal, bertanggung jawab, disiplin dan profesional dalam menjalankan tugas dan tanggung jawab yang diberikan.
								</td>
							</tr>
							<tr>
								<td>4</td>
								<td>.</td>
								<td>
									<b>Nasionalis (cinta bangsa dan tanah air).</b> Memiliki kecintaan kepada bangsa dan tanah air yang diwujudkan dengan kesediaan memberikan pengetahuan, ketrampilan dan bakatnya untuk kemajuan bangsa dan negara.
								</td>
							</tr>
							<tr>
								<td>5</td>
								<td>.</td>
								<td>
									<b>Keberagaman-kebhinekaan.</b> Memiliki toleransi dan rasa hormat kepada keberagaman, perbedaan dan kebhinekaan, baik itu agama, suku, ras, kelompok maupun golongan. Keberagaman sebagai suatu anugerah untuk membangun, untuk saling melengkapi, saling mendukung demi terwujudnya keadilan, kemakmuran dan kesejahteraan.
