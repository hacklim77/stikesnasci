				<h3 class="c-title">DIV Teknologi Laboratorium Medis</h3>
				<p class="text-justify p-indent">
					Program Studi DIV Teknologi Laboratorium Medis STIKES Nasional merupakan program studi yang didirikan pada tahun 2016 berdasarkan Keputusan Menteri Riset, Teknologi, dan Pendidikan Tinggi Nomor 116/KPT/I/2016 pada tanggal 10 maret 2016. Program Studi D-IV Teknologi Laboratorium Medis STIKES Nasional memiliki tujuan untuk menghasilkan Sarjana Terapan Teknologi Laboratorium Medis yang unggul di bidang quality control pemeriksaan Laboratorium Klinik. Masa studi program studi ini berlangsung selama 8 (delapan) semester atau 4 (empat) tahun dengan beban kredit semester (SKS) sebesar 147 SKS.
				</p>
				<h4 class="mt-3">Visi</h4>
				<p class="text-justify p-indent">
					Menjadi Program Studi D-IV Teknologi Laboratorium Medis yang bereputasi di tingkat nasional pada tahun 2025 dengan keunggulan di  bidang quality control pemeriksaan laboratorium klinik.
				</p>
