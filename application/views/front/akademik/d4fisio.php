				<h3 class="c-title">DIV Fisioterapi</h3>
				<p class="text-justify p-indent">
					DIV Fisioterapi STIKES Nasional dalam perkuliahannya ditunjang dengan berbagai sarana dan prasarana yang memadai, ruang kuliah dengan perlengkapan klasikal, laboratorium-laboratorium yang memadai untuk praktek klinis, dan penelitian ilmiah. Laboratorium yang telah dipersiapkan diantaranya adalah laboratorium Skill, Manual Terapi, dan Pediatri. Ruang praktikum pediatri, gymnasium, dan juga sedang dipersiapkan klinik pelayanan umum.

					Progdi Fisioterapi selalu berusaha memberikan kontribusi yang besar dalam pelayanan kepada mahasiswa, demi meningkatkan kualitas kemampuan mahasiswa dalam kegiatan akademik, klinis ataupun penelitian ilmiah.

					Keunggulan dan kharakteristik dari program studi D IV Fisioterapi STIKES Nasional adalah fisioterapi tumbuh kembang yang unggul dalam manajemen dan komunikasi pelayanan pasien.
				</p>
				<h4 class="mt-3">Visi</h4>
				<p class="text-justify p-indent">
					Pada tahun 2025 menjadi pusat pendidikan tenaga fisioterapi yang bereputasi di tingkat nasional dengan berfokus pada bidang pediatri, yang unggul dalam manajemen dan komunikasi pelayanan
				</p>
